var perkalian = 0;
var pembagian = 0;
var pertambahan = 0;
var pengurangan = 0;
 
function calculator(a, b) {

    perkalian = a * b
    pembagian = a / b
    pertambahan = a + b
    pengurangan = a - b
}
calculator(3, 2);
console.log(perkalian);
calculator(10, 2);
console.log(pembagian);
calculator(76, 12);
console.log(pertambahan);
calculator(789, 10);
console.log(pengurangan);